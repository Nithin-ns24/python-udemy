# Dictionary data type 

employee = {
    "name": "Jhon-de",
    "age": 28,
    "Position": "Principle engineer",
    "Salary": 50000
}
print(employee["name"])
print(employee["Position"])

# Adding the extra key values to the variable

employee["Department"] = "IT"
print(employee)

# Updating an existing value
employee["Salary"] = "90000"
print(employee)

